export function precalculatedAtPixel(px: number): ShadowElevation {
    if (px < 0 || px > 23) {
        throw new Error("Invalid elevation pixel, should be between 0 and 23");
    }

    return {
        umbra: umbraMap[px] as BoxShadow,
        penumbra: penumbraMap[px] as BoxShadow,
        ambient: ambientMap[px] as BoxShadow,
    };
}
